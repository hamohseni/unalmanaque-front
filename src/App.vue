<template>
  <NavBar v-if="!authPage" />
  <router-view></router-view>
</template>

<script>
import NavBar from '@/components/NavBar.vue';

export default {
  name: 'App',
  components: {
    NavBar,
  },
  computed: {
    authPage() {
      if (this.$route.name === 'Login' || this.$route.name === 'Register') {
        return true;
      } else {
        return false;
      }
    },
  },
};
</script>

